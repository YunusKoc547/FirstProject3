package day21;

public class NestedForLoopVideo {

    public static void main(String[] args) {

        for (int x = 0; x < 4; x++) {
            System.out.println();

            for (int j = 0; j < 3; j++) {
                System.out.println(" Row " + x + "-Column " + j);
            }

        }
    }
}
