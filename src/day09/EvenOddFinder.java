package day09;

public class EvenOddFinder {

    public static void main(String[] args) {

        int myNumber = 15;

        System.out.println(myNumber);
        if (myNumber % 2 == 0) {
            System.out.println("This number is even");
        }
        else {
            System.out.println("This number is odd");
        }



    }
}
