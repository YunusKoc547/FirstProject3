package day12;

public class WakeUpTask_CityWeather {

    public static void main(String[] args) {

        String cityWeather = "Sunny";

        if (cityWeather.equals("Sunny") ) {
            System.out.println("Code in sunny weather");
        }
        else if (cityWeather.equals("Rainy")){
            System.out.println("Code in rainy weather");
        }
        else if (cityWeather.equals("Cloudy")){
            System.out.println("Code in cloudy weather");
        }
        else if (cityWeather.equals("Snowy")) {
            System.out.println("Code in snowy weather");
        }
        else {
            System.out.println("DOES NOT MATTER, KEEP CODING ANYWAY!!");
        }



    }
}